const fullLanguageFallbacks: AppLanguage[] = ["fr", "de", "ar"];

for (const code of fullLanguageFallbacks) {
  Object.assign(translations[code], translations.en);
}

function interpolate(template: string, vars?: Record<string, string | number>) {
  if (!vars) return template;
  return template.replace(/\{\{(.*?)\}\}/g, (_, key) => String(vars[key.trim()] ?? ""));
}

export function t(language: AppLanguage, key: string, vars?: Record<string, string | number>) {
  const template = translations[language][key] ?? translations.en[key] ?? key;
  return interpolate(template, vars);
}

export function translateCategory(language: AppLanguage, category: string) {
  if (language === "en") return category;
  const mapped = categoryTranslations[category as keyof typeof categoryTranslations];
  if (!mapped) return category;
  return mapped[language as Exclude<AppLanguage, "en">] ?? category;
}

export function translateFrequency(language: AppLanguage, value: keyof typeof freqTranslations) {
  return freqTranslations[value][language] ?? freqTranslations[value].en;
}

export function getLanguageLabel(language: AppLanguage) {
  return SUPPORTED_LANGUAGES.find((item) => item.code === language)?.nativeName ?? "English";
}

export function useI18n() {
  const { appSettings } = useLedger();
  const language = SUPPORTED_LANGUAGES.some((item) => item.code === appSettings.language)
    ? appSettings.language
    : "en";

  return useMemo(
    () => ({
      language,
      t: (key: string, vars?: Record<string, string | number>) => t(language, key, vars),
      tc: (category: string) => translateCategory(language, category),
      tf: (value: keyof typeof freqTranslations) => translateFrequency(language, value),
      languageLabel: getLanguageLabel(language),
    }),
    [language]
  );
}
